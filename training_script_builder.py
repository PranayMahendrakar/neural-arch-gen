    # ------------------------------------------------------------------
    # Private helpers
    # ------------------------------------------------------------------

    @staticmethod
    def _pick_lr(family: str) -> float:
        return {"transformer": 5e-5, "resnet": 1e-4, "unet": 1e-4,
                "cnn": 1e-3, "mlp": 1e-3, "lstm": 1e-3, "tcn": 1e-3}.get(family, 1e-3)

    @staticmethod
    def _pick_epochs(tier: str) -> int:
        return {"tiny": 100, "small": 80, "medium": 60, "large": 40}.get(tier, 60)

    @staticmethod
    def _pick_loss(spec: Any) -> str:
        if spec.problem_type.lower() in ("classification", "nlp"):
            ls = 0.1 if spec.dataset_size > 10_000 else 0.0
            return f"nn.CrossEntropyLoss(label_smoothing={ls})"
        if spec.problem_type.lower() == "segmentation":
            return "nn.CrossEntropyLoss()"
        if spec.problem_type.lower() == "detection":
            return "nn.SmoothL1Loss()"
        return "nn.MSELoss()"



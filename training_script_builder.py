    warmup = torch.optim.lr_scheduler.LinearLR(opt, 1e-6/cfg["lr"], 1.0, total_iters=cfg["warmup_epochs"])
    cosine = torch.optim.lr_scheduler.CosineAnnealingLR(opt, T_max=cfg["epochs"]-cfg["warmup_epochs"])
    return torch.optim.lr_scheduler.SequentialLR(opt, [warmup, cosine], milestones=[cfg["warmup_epochs"]])

